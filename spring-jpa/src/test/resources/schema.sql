SET MODE ORACLE;

DROP SEQUENCE IF EXISTS SQ_COSTUMER;

CREATE SEQUENCE SQ_COSTUMER;

DROP TABLE IF EXISTS COSTUMER;

CREATE TABLE COSTUMER (
  ID_COSTUMER NUMBER PRIMARY KEY,
  FIRST_NAME  VARCHAR2(60) NOT NULL,
  LAST_NAME   VARCHAR2(60)
);


DROP SEQUENCE IF EXISTS SQ_ACCOUNT;

CREATE SEQUENCE SQ_ACCOUNT;

DROP TABLE IF EXISTS ACCOUNT;

CREATE TABLE ACCOUNT (
  ID_ACCOUNT   NUMBER PRIMARY KEY,
  ID_COSTUMER  NUMBER NOT NULL,
  EXPIRED_DATE DATETIME
);

ALTER TABLE ACCOUNT
ADD FOREIGN KEY (ID_COSTUMER) REFERENCES COSTUMER (ID_COSTUMER);